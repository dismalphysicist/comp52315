ForceLJ::ForceLJ(int ntypes_)
{
  cutforce = 0.0;
  use_oldcompute = 0;
  reneigh = 1;
  style = FORCELJ;
  ntypes = ntypes_;

  cutforcesq = new MMD_float[ntypes*ntypes];
  epsilon = new MMD_float[ntypes*ntypes];
  sigma6 = new MMD_float[ntypes*ntypes];
  sigma = new MMD_float[ntypes*ntypes];

  for(int i = 0; i<ntypes*ntypes; i++) {
    cutforcesq[i] = 0.0;
    epsilon[i] = 1.0;
    sigma6[i] = 1.0;
    sigma[i] = 1.0;
  }


}
